env_df.head()

"""## 4. Calculate Corrosion Exposure Indices"""

# Calculate corrosion exposure indices and advanced features
print("Calculating corrosion exposure indices and advanced features...")

# Basic corrosion exposure indices
env_df['corrosion_exposure_index'] = (
    (env_df['sulphur_dioxide_mass_mixing_ratio'] +
     env_df['nitrogen_dioxide_mass_mixing_ratio']) *
    env_df['metar_relative_humidity'] *
    env_df['total_parking_minutes'] / 1000
)

env_df['moisture_exposure_index'] = (
    env_df['metar_relative_humidity'] *
    env_df['total_parking_minutes'] / 100
)

# Bronze features: Critical exposure time
env_df['critical_exposure_time'] = (
    (env_df['total_parking_minutes'] / 60) *
    ((env_df['metar_relative_humidity'] > 60).astype(float) +
     ((env_df['metar_temperature_c'] - env_df['metar_dew_point_c']) < 3).astype(float)) / 2
)

# Silver features: Atmospheric corrosion index
env_df['atmospheric_corrosion_index'] = (
    np.log1p(env_df['sulphur_dioxide_mass_mixing_ratio'] * 1e8) * 0.3 +
    np.log1p((env_df['sea_salt_aerosol_003_05_mixing_ratio'] +
              env_df['sea_salt_aerosol_05_5_mixing_ratio'] +
              env_df['sea_salt_aerosol_5_20_mixing_ratio']) * 1e10) * 0.4 +
    (env_df['metar_relative_humidity'] / 100) * 0.3
)

# Silver features: Chemical aggressivity
env_df['chemical_aggressivity'] = (
    env_df['sulphur_dioxide_mass_mixing_ratio'] +
    env_df['nitrogen_dioxide_mass_mixing_ratio'] +
    env_df['hno3']
) * env_df['metar_relative_humidity'] / 100

print(f"\n✓ Basic indices calculated")
print(f"  Corrosion exposure index: {env_df['corrosion_exposure_index'].mean():.6f}")
print(f"  Moisture exposure index: {env_df['moisture_exposure_index'].mean():.2f}")
print(f"  Atmospheric corrosion index: {env_df['atmospheric_corrosion_index'].mean():.4f}")
print(f"  Chemical aggressivity: {env_df['chemical_aggressivity'].mean():.6f}")


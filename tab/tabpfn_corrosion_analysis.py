"""## 6. Create Training Pairs with Time-Aware Features + Gold Features (NO DATA LEAKAGE)"""

# Create training pairs with proper temporal ordering
print("Creating training pairs with time-aware feature aggregation + Gold features...")
print("IMPORTANT: Environmental features are aggregated ONLY up to reference_month")

# Filter aircraft with sufficient observation time
valid_aircraft = corr_df[corr_df['months_since_delivery'] >= 24].copy()
print(f"Aircraft with observation >= 24 months: {len(valid_aircraft)}")

pairs = []
for idx, row in valid_aircraft.iterrows():
    aircraft_id = row['aircraft_id']
    obs_month = row['months_since_delivery']
    
    # Pair 1: Corroded state (observation month)
    # Filter environmental data up to observation month
    env_data_corroded = env_df[
        (env_df['aircraft_id'] == aircraft_id) & 
        (env_df['months_since_delivery'] <= obs_month)
    ].sort_values('months_since_delivery')
    
    if len(env_data_corroded) > 0:
        # Basic aggregations
        env_features_corroded = env_data_corroded[env_features].agg(['mean', 'std']).T
        env_features_corroded.columns = ['mean', 'std']
        env_features_corroded = env_features_corroded.fillna(0)
        
        # Gold features: cumulative and temporal
        gold_aircraft_age = len(env_data_corroded)
        gold_dose_cumulative = env_data_corroded['atmospheric_corrosion_index'].sum()
        gold_dose_acceleration = env_data_corroded['atmospheric_corrosion_index'].tail(6).sum() / (gold_dose_cumulative + 1e-6)
